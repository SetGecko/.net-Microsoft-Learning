// Литералы
// Вывод литерала в одинарных кавычках - char
Console.WriteLine('b');
// Вывод длинного стринга в двойных кавычках - string
Console.WriteLine("Hello World!");
// Вывод целочичисленного литерала делается без кавычек - int
Console.WriteLine(123);
// Десятичный литерал с помощью литерального суффикса m или M - decimal
Console.WriteLine(12.3m);
// Вывод логических литералов (булевых - bool)
Console.WriteLine(true);
Console.WriteLine(false);